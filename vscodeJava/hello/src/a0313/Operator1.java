package a0313;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        int sum = a + b;
        int min = a - b;
        int mul = a * b;
        int div = a / b;
        System.out.println("a + b = " + sum);
        System.out.println("a - b = " + min);
        System.out.println("a * b = " + mul);
        System.out.println("a / b = " + div);
    }
}