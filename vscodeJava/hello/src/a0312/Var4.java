package a0312;

public class Var4 {
    public static void main(String[] args) {
        int a;
        a = 1;
        System.out.println(a);
        int b = 2;
        System.out.println(b);
        int c = 3, d = 4;
        // 변수 여러개를 한 줄에 선언 및 초기화 가능
        System.out.println(c);
        System.out.println(d);
    }
}
